class Pokemon:
    def __init__(
        self, max_hp, pokedex, name, pokemon_type, sp_atk, sp_def, atk, defense, speed
    ):
        self.max_hp = max_hp
        self.pokedex = pokedex
        self.name = name
        self.pokemon_type = pokemon_type
        self.sp_atk = sp_atk
        self.sp_def = sp_def
        self.atk = atk
        self.defense = defense
        self.speed = speed
